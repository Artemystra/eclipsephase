/**
 * The standard constructor for lists throughout the system (e.g. weapon selection)
 * @param {Array} objectList - An array with objects to be listed (for "standardSelectionList": {id, label, description, type, groupID}). !type = radio
 * @param {String} dialogType - The type of dialog to be displayed. This defines which part of the list-dialog.html is displayed ("standardSelectionList" for standard)
 * @param {number} width - defines the overlays' total width
 * @param {String} dialogTitle - The title displayed in the dialog. Has a fallback if none.
 * @param {String} headline - The headline within the dialog
 * @param {String} copy - The copy within the dialog
 * @returns 
 */
  export async function listSelection(objectList, dialogType, width, dialogTitle, headline, copy) {
    const title = dialogTitle
      ? game.i18n.localize(dialogTitle)
      : game.i18n.localize("ep2e.actorSheet.dialogHeadline.confirmationNeeded");
    const cancelButton = game.i18n.localize("ep2e.roll.dialog.button.cancel");
    const useButton = game.i18n.localize("ep2e.actorSheet.button.select");
    const template = "systems/eclipsephase/templates/chat/list-dialog.html";

    const content = await foundry.applications.handlebars.renderTemplate(template, {
      objectList,
      dialogType,
      headline,
      copy
    });

    const result = await foundry.applications.api.DialogV2.wait({
      window: { title },
      content,
      buttons: [
        {
          action: "cancel",
          label: cancelButton,
          callback: () => ({ cancelled: true })
        },
        {
          action: "select",
          label: useButton,
          default: true,
          callback: (event, button) => listSelectionOutcome(button.form)
        }
      ],
      position: { width },
      modal: true,
      rejectClose: false
    });

    return result ?? { cancelled: true };
  }

  function listSelectionOutcome(form) {
    let returnValue;

    if (form?.ItemSelect) {
      returnValue = form.ItemSelect.value;
    } else {
      returnValue = {};
      const inputs = form?.elements?.Input
        ? Array.from(form.elements.Input instanceof RadioNodeList ? form.elements.Input : [form.elements.Input])
        : [];

      for (const entry of inputs) {
        if (entry.value) {
          returnValue[entry.id] = entry.type === "number" ? Number(entry.value) : entry.value;
        }
      }
    }

    return {
      selection: returnValue
    };
  }



/**
 * A powerful constructor of joined rolls for the chat.
 * Use this in case you need to make a series of rolls and want to display them into
 * one singular chat message.
 * @param {Array} rollsArray - The array of rolls to be displayed
 * @param {Object} messageData - Object with the chat message data
 * @param {*} param2 
 * @returns - An object to be displayed in the chat
 */
async function joinDiceRollMessage(rollsArray, messageData={}, {rollMode, create=true}={}) {
  for ( const roll of rollsArray ) {
      if ( !roll._evaluated ) await roll.evaluate();
  }

  // Prepare chat data
  messageData = foundry.utils.mergeObject({
      user: game.user.id,
      type: CONST.CHAT_MESSAGE_TYPES.ROLL,
      sound: CONFIG.sounds.dice
  }, messageData);
  messageData.rolls = rollsArray;

  // Either create the message or just return the chat data
  const cls = getDocumentClass("ChatMessage");
  const msg = new cls(messageData);

  // Either create or return the data
  if ( create ) return cls.create(msg.toObject(), { rollMode });
  else {
      if ( rollMode ) msg.applyRollMode(rollMode);
      return msg.toObject();
  }
}

