/**
 * Filters actor for a specific flagged active effect
 * @param {Object} actor - The actor object
 * @param {String} scope - Usually "eclipsephase" if generated by the system
 * @param {String} key - Usually "effectKey" if generated by the system
 * @param {Array} values - An array with your flags to be deleted (e.g. [str1, str2, ...]). str1/str2 are strings in nature
 */
export async function tempEffectDeletion(actor, scope, key, values) {
  const effectsToDelete = actor.effects
    .filter(effect => values.includes(effect.getFlag(scope, key)))
    .map(effect => effect.id);

  if (effectsToDelete.length > 0) {
    await actor.deleteEmbeddedDocuments("ActiveEffect", effectsToDelete);
  }
}

