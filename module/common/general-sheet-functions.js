/**
 * Toggles the active state of an item and its effects.
 * This is the main function to handle automatic active effect
 * toggle on morph switch.
 * @param {Object} html - The html object of the actor sheet
 * @param {Object} actor - The actor object
 */
export function embeddedItemToggle(html, actor) {
  html.querySelectorAll(".equipped.checkBox").forEach(element => {
    element.addEventListener("click", async ev => {
      let updateData = {};
      let toggle;
      let path;
      const data = ev.currentTarget.dataset;
      const itemId = data.itemId;
      const item = actor.items.get(itemId);
      if (!item) return;

      //checks whether a radiomenu and toggles everything that's not the currentTarget off
      if (data.grouppath) {
        path = foundry.utils.getProperty(item, data.grouppath);
        for (let entry in path) {
          if (entry != data.currenttargetkey) {
            let updatePath = data.grouppath + "." + entry + ".active";
            updateData[updatePath] = false;
          }
          else {
            let updatePath = data.grouppath + "." + entry + ".active";
            updateData[updatePath] = true;
          }
        }
        await item.update(updateData);
        return;
      }

      //toggles the checkbox on/off
      if (!data.path) {
        toggle = !item.system.active;
        path = "system.active";
      }
      else {
        path = data.path;
        toggle = !foundry.utils.getProperty(item, path);
      }

      updateData[path] = toggle;
      await item.update(updateData);
    });
  });
}

