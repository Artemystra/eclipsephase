/**
 * Foundry VTTs active effect creation & deletion functions
 * @param {Object} html - The HTML object to which the event listeners are added
 * @param {*} callerobj 
 */
export function registerEffectHandlers(html, callerobj) {
  html.querySelectorAll(".effect-create").forEach(button => {
    button.addEventListener("click", async () => {
      await callerobj.createEmbeddedDocuments("ActiveEffect", [{
        name: callerobj.name,
        label: callerobj.name,
        icon: "/icons/svg/mystery-man.svg"
      }]);
    });
  });

  html.querySelectorAll(".effect-edit").forEach(button => {
    button.addEventListener("click", event => {
      const li = event.currentTarget.closest(".effect");
      if (!li) return;

      const effectId = li.dataset.itemId;
      if (!effectId) return;

      const effect = callerobj.getEmbeddedDocument("ActiveEffect", effectId);
      effect?.sheet?.render(true);
    });
  });

  html.querySelectorAll(".effect-delete").forEach(button => {
    button.addEventListener("click", async event => {
      const askForOptions = event.shiftKey;
      const li = event.currentTarget.closest(".effect");
      if (!li) return;

      const effectId = li.dataset.itemId;
      if (!effectId) return;

      if (!askForOptions) {
        const itemName = li.dataset.itemName ?? null;
        const popUpTitle = game.i18n.localize("ep2e.actorSheet.dialogHeadline.confirmationNeeded");
        const popUpHeadline = `${game.i18n.localize("ep2e.actorSheet.button.delete")} ${itemName ?? ""}`;
        const popUpCopy = "ep2e.actorSheet.popUp.deleteCopyGeneral";
        const popUpInfo = "ep2e.actorSheet.popUp.deleteAdditionalInfo";

        const popUp = await confirmation(popUpTitle, popUpHeadline, popUpCopy, popUpInfo);

        if (popUp.confirm === true) {
          await callerobj.deleteEmbeddedDocuments("ActiveEffect", [effectId]);
        }
      } else {
        await callerobj.deleteEmbeddedDocuments("ActiveEffect", [effectId]);
      }
    });
  });
}

